def generate_secret_key() -> str:
    """Generate a secure random secret key (32 bytes, URL-safe base64 encoded)."""
    return secrets.token_urlsafe(32)


def generate_encryption_key() -> str:
    """Generate a secure Fernet encryption key."""
    return Fernet.generate_key().decode()


def generate_jwt_secret() -> str:
    """Generate a secure random JWT secret key."""
    return secrets.token_urlsafe(32)


def check_postgresql() -> bool:
    """Check if PostgreSQL is accessible on localhost."""
    try:
        import socket
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(2)
        result = sock.connect_ex(('localhost', 5432))
        sock.close()
        return result == 0
    except Exception:
        return False


def check_redis() -> bool:
    """Check if Redis is accessible on localhost."""
    try:
        import socket
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(2)
        result = sock.connect_ex(('localhost', 6379))
        sock.close()
        return result == 0
    except Exception:
        return False


def backup_env_file(env_path: Path) -> None:
    """Backup existing .env file with timestamp."""
    if env_path.exists():
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        backup_path = env_path.parent / f".env.backup.{timestamp}"
        shutil.copy(env_path, backup_path)
        print(f"✓ Backed up existing .env to {backup_path}")


def get_user_input(prompt: str, default: str = "") -> str:
    """Get user input with optional default value."""
    if default:
        user_input = input(f"{prompt} [{default}]: ").strip()
        return user_input if user_input else default
    else:
        return input(f"{prompt}: ").strip()


