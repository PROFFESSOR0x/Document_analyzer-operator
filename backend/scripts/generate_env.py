def generate_env_file(args) -> bool:
    """Generate the .env file."""
    env_path = Path(__file__).parent.parent / ".env"
    
    # Check if .env exists and backup if needed
    if env_path.exists() and not args.force:
        if args.interactive:
            response = input(f"⚠ {env_path} already exists. Overwrite? (y/N): ").strip().lower()
            if response != 'y':
                print("✓ Keeping existing .env file")
                return False
        else:
            print(f"⚠ {env_path} already exists. Use --force to overwrite or --interactive for prompts")
            return False
    
    # Backup existing file
    if env_path.exists():
        backup_env_file(env_path)
    
    # Generate content
    env_content = generate_env_content(args)
    
    # Write to file
    env_path.write_text(env_content)
    
    print(f"✓ Generated .env file at {env_path}")
    print(f"✓ SECRET_KEY generated securely (32 bytes)")
    print(f"✓ ENCRYPTION_KEY generated securely (Fernet)")
    print(f"✓ JWT_SECRET_KEY generated securely")
    print(f"✓ DATABASE_URL configured for PostgreSQL at localhost:5432")
    print(f"✓ REDIS_URL configured for Redis at localhost:6379")
    
    # Service status
    print("\n📊 Service Detection:")
    if check_postgresql():
        print("  ✓ PostgreSQL detected on localhost:5432")
    else:
        print("  ⚠ PostgreSQL not detected on localhost:5432")
        print("    Install: https://www.postgresql.org/download/")
    
    if check_redis():
        print("  ✓ Redis detected on localhost:6379")
    else:
        print("  ⚠ Redis not detected on localhost:6379")
        print("    Install: https://redis.io/download/")
    
    print("\n⚠ Review and configure LLM API keys as needed:")
    print("  - OPENAI_API_KEY (https://platform.openai.com/api-keys)")
    print("  - ANTHROPIC_API_KEY (https://console.anthropic.com/settings/keys)")
    print("  - GROQ_API_KEY (https://console.groq.com/keys)")
    print("  - TOGETHER_API_KEY (https://api.together.ai/settings/api-keys)")
    print("  - ANYSCALE_API_KEY (https://app.endpoints.anyscale.com/credentials)")
    
    print("\n✓ Environment file generation complete!")
    return True


